using System;

public class Program
{
    static void Main(){
     int n = Int32.Parse(Console.ReadLine());
     int i = 1;
     
     while(i <= n){
       int j = 0;
       while (j <= n){
         Console.Write(i);
         j = j + 1;
       }
       Console.WriteLine();
       i = i + 1;
     }
     
    }
}