using System;

public class Program
{
    static void Main(){
      int n = Int32.Parse(Console.ReadLine());
      int i = 0;
      while(i < n){
        int j = 0;
        while(j < n){
          Console.Write("*");
          j = j + 1;
        }
        Console.WriteLine();
        i = i + 1;
      }
    }
}